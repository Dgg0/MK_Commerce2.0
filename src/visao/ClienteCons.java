    private ClienteFrm clienteFrm;
    
    
    private void formatarColumnTable() {
        FontMetrics fm = jTblCliente.getFontMetrics(jTblCliente.getFont());
        jTblCliente.setColumnModel(new ClienteColumnModel(fm));
    }
    
    private ClienteTableModel model = new ClienteTableModel();
    private void setListaCliente(List<Cliente> listaCliente) {
        this.jTblCliente.setModel(model);
        model.addListaDeCliente(listaCliente);
        this.formatarColumnTable();
    }
    
    private Cliente getClienteSelecionado() {
        return model.getCliente(jTblCliente.getSelectedRow());
    }
    
    private String[] param = new String[2];
    private void getParametroPesquisa() {
        param[0] = jComboPesquisarPor.getSelectedItem().toString().toUpperCase();
        param[1] = jTxfParametrosPesquisa.getText().toUpperCase();
    }
    
    private void botaoPesquisar() {
        this.getParametroPesquisa();
        if (clienteDao == null) {
            clienteDao = new ClienteDao();
        }
        listaCliente = new ArrayList<>();
        
        if (param[0].equals("NOME")) {
            listaCliente = clienteDao.selectNome(param[1]);
        } else if (param[0].equals("CPF")) {
            listaCliente = clienteDao.selectCpf(param[1]);
        } else {
            listaCliente.add(clienteDao.selectId(Integer.parseInt(param[1])));
        }
        setListaCliente(listaCliente);
    }
    
    private void botaoNovo() {
        if (clienteFrm == null) {
            clienteFrm = new ClienteFrm(new JFrame(), true);
        }
        clienteFrm.setCliente(new Cliente());
        clienteFrm.setVisible(true);
    }
    
    private void botaoEditar() {
        if (clienteFrm == null) {
            clienteFrm = new ClienteFrm(new JFrame(), true);
        }
        clienteFrm.setCliente(getClienteSelecionado());
        clienteFrm.setVisible(true);
    }
