    private void formatarTableModelCliente() {
        FontMetrics fm = jTblCliente.getFontMetrics(jTblCliente.getFont());
        jTblCliente.setColumnModel(new ClienteColumnModel(fm));
    }
    
    private ClienteTableModel model = new ClienteTableModel();
    private void setListaCliente(List<Cliente> listaCliente) {
        jTblCliente.setModel(model);
        model.addListaDeCliente(listaCliente);
        this.formatarTableModelCliente();
    }
    
    public Cliente getClienteSelecionado() {
        return model.getCliente(jTblCliente.getSelectedRow());
    }
    
    private String param[] = new String[2];
    private void getParametrosPesquisa() {
        param[0] = jComboParametros.getSelectedItem().toString().toUpperCase();
        param[1] = jTxfPesquisar.getText().toUpperCase();
    }
    
    private void botaoPesquisar() {
        getParametrosPesquisa();
        if (clienteDao == null) {
            clienteDao = new ClienteDao();
        }
        listaCliente = new ArrayList<>();
        if (param[0].equals("NOME")) {
            listaCliente = clienteDao.selectNome(param[1]);
        } else {
            listaCliente = clienteDao.selectCpf(param[1]);
        }
        setListaCliente(listaCliente);
    }

    public Cliente getCliente() {
        return cliente;
    }

