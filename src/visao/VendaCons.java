    public void buscarItemVenda() {
        if (itemVendaDao == null) {
            itemVendaDao = new ItemVendaDao();
        }
        listaItemVenda = new ArrayList<>();
        
        listaItemVenda = itemVendaDao.selectId(getVendaSelecionada().getIdVenda());
        setListaItemVenda(listaItemVenda);
    }
