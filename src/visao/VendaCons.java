    String param[] = new String[3];
    private void getParametrosPesquisa() {
        param[0] = jComboBoxParam.getSelectedItem().toString();
        param[1] = jTxfPesquisar.getText();
    }
    
    private void botaoPesquisar() {
        getParametrosPesquisa();
        if (vendaDao == null) {
            vendaDao = new VendaDao();
        }
        List<Venda> listaVenda = new ArrayList<>();
        
        if (param[0].equals("Cliente")) {
            if (cliente != null) {
                listaVenda = vendaDao.selectCliente(cliente.getId());
            } else {
                JOptionPane.showMessageDialog(null, "Selecione o CLIENTE! ");
            }
        } else if (param[0].equals("ID")) {
            listaVenda.add(vendaDao.selectId(Integer.parseInt(param[1])));
        } else {
            if (jDateChooser.getDate() != null) {
                listaVenda = vendaDao.selectData(jDateChooser.getDate());
            } else {
                JOptionPane.showMessageDialog(null, "Selecione a DATA");
            }
        }
        setListaVenda(listaVenda);
    }
    
    private void botaoBuscarCliente() {
        BuscarClienteCons bc = new BuscarClienteCons(new JFrame(), true);
        bc.setVisible(true);
        cliente = new Cliente();
        cliente = bc.getCliente();
        jTxfPesquisar.setText(cliente.getNome());
    }
    
