    public void setMarca(Marca marca) {
        this.marca = marca;
        this.mostrarDadosFrom();
    }
    
    private void iniciarFrom() {
        marca = new Marca();
        this.limparDadosFrom();
        jBtnNovo.setEnabled(false);
        jBtnSalvar.setEnabled(true);
        jBtnExcluir.setEnabled(false);
        jTxfId.setEditable(false);
    }
    
    private void limparDadosFrom() {
        jTxfId.setText("");
        jTxfNome.setText("");
    }
    
    private void mostrarDadosFrom() {
        if (marca != null) {
            if (marca.getId() != null) {
                jTxfId.setText(String.valueOf(marca.getId()));
            } else {
                jTxfId.setText("");
            }
            jTxfNome.setText(marca.getNome());
        } else {
            limparDadosFrom();
        }
        if (marca.getId() != null) {
            jBtnNovo.setEnabled(true);
            jBtnSalvar.setEnabled(true);
            jBtnExcluir.setEnabled(true);
        } else {
            jBtnNovo.setEnabled(false);
            jBtnSalvar.setEnabled(true);
            jBtnExcluir.setEnabled(false);
        }
    }
    
    private void obterDadosFrom() {
        this.marca.setNome(jTxfNome.getText());
    }
    
    private void botaoNovo() {
        marca = new Marca();
        jBtnNovo.setEnabled(false);
        jBtnSalvar.setEnabled(true);
        jBtnExcluir.setEnabled(false);
        this.mostrarDadosFrom();
    }
    
    private void botaoSalvar() {
        if (marcaDao == null) {
            marcaDao = new MarcaDao();
        }
        obterDadosFrom();
        if ((marca.getNome() == null) || marca.getNome().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o nome da marca!");
        } else {
            marcaDao.salvar(marca);
            botaoNovo();
        }
    }
    
    private void botaoExcluir() {
        if (marcaDao == null) {
            marcaDao = new MarcaDao();
        }
        marcaDao.excluir(marca);
        
        jBtnNovo.setEnabled(false);
        jBtnSalvar.setEnabled(true);
        jBtnExcluir.setEnabled(false);
        marca = new Marca();
        
        this.limparDadosFrom();
    }
